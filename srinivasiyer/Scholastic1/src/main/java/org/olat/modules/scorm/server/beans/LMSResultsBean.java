package org.olat.modules.scorm.server.beans;

/**
 * The "output" bean - generated by the action class in struts and used to pass information back to the updating hidden frame in the scorm player frameset
 * 
 * @author Paul Sharples
 */
public class LMSResultsBean {

	private final String itemID;
	private final String isSco;
	private final String[][] cmiStrings;
	private final String isUpdating;
	private final String[][] preReqTable;
	private final String isItemCompleted;
	private final String isCourseCompleted;
	private final String hasPrerequisites;

	public LMSResultsBean(final String itemID, final String isSco, final String[][] cmiStrings, final String isUpdating, final String preReqTable[][],
			final String isItemCompleted, final String isCourseCompleted, final String hasPrerequisites) {
		this.itemID = itemID;
		this.isSco = isSco;
		this.cmiStrings = cmiStrings;
		this.isUpdating = isUpdating;
		this.preReqTable = preReqTable;
		this.isItemCompleted = isItemCompleted;
		this.isCourseCompleted = isCourseCompleted;
		this.hasPrerequisites = hasPrerequisites;
	}

	public String getItemID() {
		return (itemID);
	}

	public String getIsSco() {
		return (isSco);
	}

	public String[][] getCmiStrings() {
		return cmiStrings;
	}

	public String getIsUpdating() {
		return isUpdating;
	}

	public String[][] getPreReqTable() {
		return preReqTable;
	}

	public String getIsItemCompleted() {
		return isItemCompleted;
	}

	public String getIsCourseCompleted() {
		return isCourseCompleted;
	}

	public String getHasPrerequisites() {
		return hasPrerequisites;
	}
}
